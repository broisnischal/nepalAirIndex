                            <li>REST APIs</li>
                            <li>REST APIs</li>

                        </ul>
                        <p>
                            An API is defined as a specification of possible interactions with a software component.
                            What does that mean, exactly? Well, imagine that a car was a software component. Its API
                            would include information about what it can do—accelerate, brake, turn on the radio, etc. It
                            would also include information about how you could make it do those things. For instance, to
                            accelerate, you put your foot on the gas pedal and push.

                            The API doesn’t have to explain what happens inside the engine when you put your foot on the
                            accelerator. That’s why, if you learned to drive a car with an internal combustion engine,
                            you can get behind the wheel of an electric car without having to learn a whole new set of
                            skills. The what and how information come together in the API definition, which is abstract
                            and separate from the car itself.
                        </p>
